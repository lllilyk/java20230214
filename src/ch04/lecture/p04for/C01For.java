package ch04.lecture.p04for;

public class C01For {
	public static void main(String[] args) {
		// 반복문 while
		// 1변수초기화, 2조건 확인, 3조건이 true이면 코드블럭 실행, 4변수값 변경
		
		for (int i = 0; i <3 ; i++) { 
			System.out.println("반복할 코드1");
			System.out.println("반복할 코드2");
		}
		
		System.out.println("실행 이어감..");
	
	}

}
